#include "filler.h"

void	free2d(char **s)
{
	int i;

	i = 0;
	while (s[i])
		free(s[i++]);
	free(s);
}

void	free2dint(int **s)
{
	int i;

	i = 0;
	while (s[i])
		free(s[i++]);
	free(s);
}

